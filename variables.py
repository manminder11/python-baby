# comments in python
# Comments can be used to explain Python code.
# Comments can be used to make the code more readable.


# types of comments
# 1. Single line comment
# 2. Multi-line comment
# 3. Documentation string
# Single line comment
# Single line comment is written using the hash symbol (#)
# Example:
# # This is a single line comment
# print("Hello, World!")





# Multi-line comment
# Multi-line comment is written using triple quotes (''' or """)    
# Example:
# '''
